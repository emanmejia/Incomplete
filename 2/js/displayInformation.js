// Both console.log() and alert() can be used to display information to the user.
// Unlike alert(), console.log() does not stop program execution and is often a better choice.

// console.log() can also display several comma-seperated values at once.
const temp1 = 36.9;
const temp2 = 37.6;
const temp3 = 37.1;
console.log(temp1, temp2, temp3); // Show "36.9 37.6 37.1"