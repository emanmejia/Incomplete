let a;
a = 3.14
console.log(a); // 3.14

let b = 5;
console.log(b); // 5

// override it, after you declared a and b. 
a = 70;
console.log(a); // 70

b = 10;
console.log(b); // 10

// declare c and add a + b for c's result. 
let c = a + b;
console.log(c)  // 10 + 70 = 80

const d = 5;
// d = 10;  commented out because you cannot change a constant value. 
console.log(d); // 5


// Increment a number variable.

let e = 0; // 0
e += 1; // 0 + 1 = 1
e++; // 1 + 1 = 2
console.log(e)



