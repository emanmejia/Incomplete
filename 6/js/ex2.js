// Programming with Objects

// RPG characters have attributes like strength, stamina or intelligence.  
/* In our example, a character will have three attributes:
- their name
- their health (number of life points)
- their strength
*/

// Example:
const aurora = {
  name: "Aurora",
  health: 150,
  strength: 25
};

// You can assign numbers, strings or even other objects to properties. 

console.log(`${aurora.name} has ${aurora.health} health points and ${aurora.strength} as strength.`);